        <p>Em 2008, dez municípios do Paraná foram selecionados para receber unidades do Centro Estadual de Educação Profissional (CEEP). Laranjeiras do Sul foi um dos municípios contemplados, devido à sua localização central, atendendo à demanda regional. O CEEP na região Cantuquiriguaçu foi planejado para atender áreas específicas como o maior assentamento da América Latina e a maior Terra Indígena do Paraná.

          Estudos de demanda foram realizados para justificar a necessidade da unidade de educação profissional, envolvendo alunos do Ensino Fundamental e Médio. Diversas reuniões foram realizadas com entidades municipais e secretarias de educação para discutir a oferta e demanda de cursos. Os cursos escolhidos foram: Técnico em Contabilidade, Técnico em Edificações, Técnico em Agronegócio e Técnico em Enfermagem.

          A construção do CEEP começou em 2011, mas foi interrompida em 2013 devido a problemas com o Departamento Nacional de Infraestrutura de Transporte (DNIT). Em 2016, a professora Eliane Maria da Silva foi nomeada a primeira diretora do centro. Com sua equipe, ela trabalhou arduamente para preparar a infraestrutura e iniciar as atividades.
          Em agosto de 2016, os primeiros cursos começaram a funcionar no período noturno. Em fevereiro de 2017, o primeiro curso Técnico em Agronegócio Integrado começou no período da manhã. A instituição tem capacidade para atender 1200 alunos, com uma área construída de 6.495,56 m² e uma área total de 14.354,15 m².

          Inicialmente chamado de "Centro Estadual de Educação Profissional de Laranjeiras do Sul", o nome foi mudado para homenagear a Professora Naiana Babaresco de Souza, uma servidora pública dedicada à educação e que enfrentou o câncer com determinação. A escolha do nome reflete um referencial jovem e serve como homenagem à servidora.</p>
